package first;

public class switchstat {

	public static void main(String[] args) {
		// Switch Statement
		int x=40;
		switch(x) {
		case 10:System.out.println("10"); 
				break;
		case 20:System.out.println("20"); 
				break;
		case 30:System.out.println("30"); 
				break;		
		default:System.out.println("Invalid"); 
				break;		
		}
			
	}

}
